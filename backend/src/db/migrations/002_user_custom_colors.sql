-- 002_user_custom_colors.sql
-- task14: у пользователя появляется личная палитра из сохранённых цветов,
-- доступная с любого устройства. Храним как TEXT[] из hex-строк `#rrggbb`.
-- Валидация (длина ≤10, формат, дедупликация) остаётся на уровне приложения,
-- CHECK-констрейнты здесь только на тип элементов и общий размер массива —
-- чтобы даже «поехавший» клиент не мог положить мегабайтное поле в строку
-- пользователя.

ALTER TABLE users
  ADD COLUMN custom_colors TEXT[] NOT NULL DEFAULT '{}';

-- Жёсткий верхний потолок: до 20 элементов (бизнес-лимит 10, берём запас,
-- чтобы миграция не упала, если клиент когда-то успел сохранить больше).
-- По длине одной строки ограничивается нашим регэкспом в route-слое.
ALTER TABLE users
  ADD CONSTRAINT users_custom_colors_len
  CHECK (array_length(custom_colors, 1) IS NULL OR array_length(custom_colors, 1) <= 20);
